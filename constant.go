package main

import "fmt"
import "math"

// const supports strin, boolean, char and numeric vals
const s string = "constant"

func main() {
	fmt.Println(s)
	// can be inside or outside a func just like var
	const n = 500000000

	const d = 3e20 / n
	fmt.Println(d)

	fmt.Println(int64(d))

	fmt.Println(math.Sin(n))
}
