package main

import "fmt"

func main() {

	var a = "initial"
	fmt.Println(a)

	// declaring multiple variable at once
	var b, c int = 1, 2
	fmt.Println(b, c)

	var d = true
	fmt.Println(d)

	// zero valued variable >> prints 0
	var e int
	fmt.Println(e)

	// this is equal to f strin = "short"
	// only inside function
	f := "short"
	fmt.Println(f)
}
